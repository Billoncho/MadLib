# Creates a madlib using adjectives, nouns and verbs.

# Asks the user to input an adjective.
adjective = input("Please enter an adjective: ")

# Asks the user to input a noun.
noun = input("Please enter a noun: ")

# Asks the user to input a verb ending in -ed.
verb = input("Please enter a verb ending in -ed: ")

# Prints "Your Madlib and then the sentence created by the user's inputs.
print("Your MadLib: ")
print("The", adjective, noun, verb, "over the lazy brown dog.")
